//This program implements a for loop that counts from 0 to 10

//include the iostream directive
#include <iostream>

//use the standard namespace
using namespace std;

int main()
{
    //for loop that counts from 0 to 10
    for (int count = 0; count <= 10; count++)
    {
        //print out each iteration of the loop
        cout << count << endl;

    }
    return 0;

}